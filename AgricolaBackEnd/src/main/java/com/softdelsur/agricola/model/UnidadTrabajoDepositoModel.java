package com.softdelsur.agricola.model;


public class UnidadTrabajoDepositoModel {
	
	private String id;
	private String idUnidadTrabajo;
	private String idDeposito;
	private int codigoDeposito;
	private String descripcionDeposito;
	
	
	
	public UnidadTrabajoDepositoModel() {
		super();
	}
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getIdUnidadTrabajo() {
		return idUnidadTrabajo;
	}
	public void setIdUnidadTrabajo(String idUnidadTrabajo) {
		this.idUnidadTrabajo = idUnidadTrabajo;
	}
	public String getIdDeposito() {
		return idDeposito;
	}
	public void setIdDeposito(String idDeposito) {
		this.idDeposito = idDeposito;
	}
	public int getCodigoDeposito() {
		return codigoDeposito;
	}
	public void setCodigoDeposito(int codigoDeposito) {
		this.codigoDeposito = codigoDeposito;
	}
	public String getDescripcionDeposito() {
		return descripcionDeposito;
	}
	public void setDescripcionDeposito(String descripcionDeposito) {
		this.descripcionDeposito = descripcionDeposito;
	}
	
	
	
	

}
